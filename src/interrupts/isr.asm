; ISR stub macro - no error code
%macro ISR_NOERRCODE 1
isr%1:
    cli
    push byte 0
    push byte %1
    jmp isr_common_stub
%endmacro

; ISR stub macro - with error code
%macro ISR_ERRCODE 1
isr%1:
    cli
    push byte %1
    jmp isr_common_stub
%endmacro

; IRQ stub macro
%macro IRQ 2
irq%1:
    cli
    push byte 0
    push byte %2
    jmp irq_common_stub
%endmacro

